package it.uniupo.simnova.service.export.helper.pdf;

/**
 * Classe di supporto per le costanti utilizzate nella generazione di PDF.
 *
 * @author Alessandro Zappatore
 * @version 1.0
 */
public class PdfConstant {
    /**
     * Dimensione del font per l'intestazione del PDF (es. titolo scenario).
     */
    public static final float HEADER_FONT_SIZE = 14;
    /**
     * Spaziatura verticale tra le righe di testo (interlinea).
     */
    public static final float LEADING = 14;
    /**
     * Margine laterale e superiore/inferiore della pagina in punti.
     */
    public static final float MARGIN = 40;
    /**
     * Dimensione del font per il corpo del testo.
     */
    public static final float BODY_FONT_SIZE = 11;
    /**
     * Dimensione del font per eventuali note o testo secondario.
     */
    public static final float SMALL_FONT_SIZE = 9;
    /**
     * Dimensione del font per il titolo principale del PDF.
     */
    public static final float TITLE_FONT_SIZE = 16;
}

